import os

_PATH = os.path.abspath(os.path.realpath(__file__))[2:-8].replace('\\', '/')
print(_PATH)


######################################################################################################################################################################
######################################################################################################################################################################


try:
    os.system('pip3 install --upgrade pip')
    os.system('pip3 install -r '+_PATH+'requirements.txt')
except:
    os.system('sudo pip3 install --upgrade pip')
    os.system('sudo pip3 install -r '+_PATH+'requirements.txt')


